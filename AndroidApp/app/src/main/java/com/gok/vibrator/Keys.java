package com.gok.vibrator;

public class Keys {

    String key;
    String ID;

    public Keys(String key, String ID) {
        this.key = key;
        this.ID = ID;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

}
